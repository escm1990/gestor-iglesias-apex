CREATE TABLE BIEN (
    ID_BIEN          NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_IGLESIA       NUMBER NOT NULL,
    NOMBRE           VARCHAR2(150) NOT NULL,
    DESCRIPCION      VARCHAR2(300),
    VALOR_ESTIMADO   NUMBER(12,2),
    ESTADO           VARCHAR2(50) DEFAULT 'ACTIVO',
    -- Auditor√≠a
    USUARIO_INGRESO  VARCHAR2(50) NOT NULL,
    FECHA_INGRESO    DATE DEFAULT SYSDATE NOT NULL,
    USUARIO_MODIFICA VARCHAR2(50),
    FECHA_MODIFICA   DATE,
    CONSTRAINT PK_BIEN PRIMARY KEY (ID_BIEN),
    CONSTRAINT FK_BIEN_IGLESIA FOREIGN KEY (ID_IGLESIA)
        REFERENCES IGLESIA(ID)
);